package com.example.ankita.tseccanteenuser;

public class SendProducts {
    String n,q,p;
    public SendProducts(String n, String q, String p){
        this.n=n;
        this.q=q;
        this.p=p;
    }

    public String getN() {
        return n;
    }

    public void setN(String n) {
        this.n = n;
    }

    public String getQ() {
        return q;
    }

    public void setQ(String q) {
        this.q = q;
    }

    public String getP() {
        return p;
    }

    public void setP(String p) {
        this.p = p;
    }
}
